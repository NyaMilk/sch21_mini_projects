void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int i;
	int c;

	i = 0;
	c = 1;
	while (c < ac)
	{
		while (av[c][i] && ac)
		{
			ft_putchar(av[c][i]);
			i++;
		}
		c++;
		i = 0;
		ft_putchar('\n');
	}
	return (0);
}
